% character string
fprintf('Rover\n')

% character string variable
dog_name = 'Rover';
fprintf('%s\n', dog_name)

% fancier character string variable
fprintf('The name of the dog is %s\n', dog_name)

% integers
i = 7;
j = 8;
fprintf('The value of i is %d\n', i)

% multiple integers
fprintf('i = %d, j =%d\n', i, j)

% float
myfloat = 7.159;
fprintf('The value of my float is %f\n', myfloat)

% specify number decimal places
fprintf('The value of my float is %5.3f\n', myfloat)

% scientific notation
mysci = 7594.88;
fprintf('mysci = %9.3e\n', mysci)
